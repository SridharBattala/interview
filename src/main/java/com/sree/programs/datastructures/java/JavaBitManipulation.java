package com.sree.programs.datastructures.java;

public class JavaBitManipulation {
	public static void main(String[] args) {
		System.out.println("convert int to binary= " + Integer.toBinaryString(10));
		System.out.println("convert int to binary= " + Long.toBinaryString(10));
		System.out.println("convert binary to int = " + Integer.parseInt("1010", 2));
		System.out.println("convert binary to int = " + Long.parseLong("1010", 2));
	}
}
